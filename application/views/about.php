<h1>Om Hig Debatt</h1>
<p>HiG Debatt = Diskusjonsarena for studenter ved HiG, hvor de kan diskutere tema som vedrører
skolen og studentene.</p>
<p>Tilbakemelding som kan gis på hvert enkelt innlegg:</p>
<ul>
	<li>Informativt +1</li>
	<li>Interessant +1</li>
	<li>Godt skrevet +1</li>
	<li>Skeptisk -1</li>
	<li>Uenig +1</li>
	<li>Useriøst -2</li>
	<li>Relevant +1</li>
	<li>Enig +1</li>
	<li>Irrelevant -1</li>
</ul>
<p>Høyeste score som kan gis til et innlegg eller en kommentar er 4 poeng.</p>